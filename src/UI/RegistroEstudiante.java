    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!areCamposEmpty()) {
            matricula = txtMatricula.getText();
            nombre = txtNombre.getText();
            paterno = txtPaterno.getText();
            materno = txtMaterno.getText();
            grado = (int) spnGrado.getValue();
//            String grupo = (String) boxGrupo.getSelectedItem();
            String grupo = "C";
            Logic.conexion con = new Logic.conexion();
            con.ConectarBasedeDatos();

            Statement statement = con.sentencia;
            try {
                statement.executeUpdate("INSERT INTO alumno "
                        + "(matricula, nombre, apellido_paterno,"
                        + "apellido_materno, grado, grupo)"
                        + " VALUES ('" + matricula + "','"
                        + nombre + "','" + paterno
                        + "','" + materno + "', " + grado + ", '" + grupo + "');");
            } catch (SQLException ex) {
                System.out.println(ex);
            }
//            Estudiantes.btnRegistro.setEnabled(true);
//            Estudiantes.mostrarTablaAlumnos();
            //Estudiantes.mostrarNumeroDeEstudiatnes();
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Complete los campos necesarios.", "Error", 0);
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

