    private void btn_Aceptar_ingresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Aceptar_ingresoActionPerformed
        try{
        Logic.conexion cone = new Logic.conexion();
        
        String usuario = txt_usuario.getText();
        String contrasena = txt_password.getText();
        
        cone.ConectarBasedeDatos();
        
        String sql = "SELECT COUNT(*) AS i FROM personal WHERE usuario = '"+usuario+"' and contrasena='"+contrasena+"'";
        cone.resultado = cone.sentencia.executeQuery(sql);
        
        while (cone.resultado.next()){
            if(cone.resultado.getString("i").equals("1")){
                  
                if(usuario.equals("admin")){
                Menu adm = new Menu();
                adm.lbl_menu_nombre.setText(usuario);
                adm.setVisible(true);
                }
                else{
                Menu m = new Menu();
                Menu.lbl_menu_nombre.setText(usuario);
                m.setVisible(true);
                }
                
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Usuario o clave incorrectas");
            }
            
        }
        cone.DesconectarBasedeDatos();
        }catch(SQLException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_btn_Aceptar_ingresoActionPerformed

